#include <stdio.h>

int main()
{

    int a = 13;
    int b = 8;

    printf("a = %d\tb = %d\n\n", a, b);
    printf("Adição: a + b = %d\n", a + b);
    printf("Subtração: a - b = %d\n", a - b);
    printf("Multiplicação: a * b = %d\n", a * b);
    printf("Divisão: a / b = %d\n", a / b);
    printf("Módulo: a %% b = %d\n", a % b);

    return 0;
}